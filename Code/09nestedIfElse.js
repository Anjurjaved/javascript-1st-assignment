// Nested If else
let number = -1;
if(number>=80){
    console.log("You got A+")
}
else if(number>=70){
    console.log("You got A")
}
else if(number>=60){
    console.log("You got B")
}
else if(number>=50){
    console.log("You got C")
}
else if(number>=40){
    console.log("You got D")
}
else
{
    console.log("You failed in the exam")
}